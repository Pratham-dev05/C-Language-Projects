#include <stdio.h>
int main()
{
    int a=10, b=3;
    
    // if value is true then it will return 1 otherwise it will return 0.
    
    printf("a==b: %d\n", a==b);
    printf("a!=b: %d\n", a!=b);
    printf("a>b: %d\n", a>b);
    printf("a<b: %d\n", a<b);
    printf("a>=b: %d\n", a>=b);
    printf("a<=b: %d\n", a<=b);
    
    return 0;
}
